class SwitchStatement{
	public static void main(String args[]){
	int age;
	age = 3;
	switch (age){
	case 1:
	System.out.println("You can crawl");
	break;
	case 2:
	System.out.println("You can walk");
	break;
	case 3:
	System.out.println("You can run");
	break;
	default:
	System.out.println("You can boss");
	}
     }
}