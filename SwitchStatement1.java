class SwitchStatement1{
	public static void main(String args[]){
	int age;
	age = 2;
	switch (age){
	case 1:
	System.out.println("You can crawl");
	
	case 2:
	System.out.println("You can walk");
	
	case 3:
	System.out.println("You can run");

	default:
	System.out.println("You are boss");
	}
     }
}